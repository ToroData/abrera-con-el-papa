window.I18N_STATE = {
  lang: localStorage.getItem('abrera.lang') || 'ca',
  listeners: [],
};

window.t = function (key) {
  const dict = window.I18N[window.I18N_STATE.lang] || window.I18N.ca;
  return dict[key] || key;
};

window.setLang = function (lang) {
  if (!window.I18N[lang]) return;
  window.I18N_STATE.lang = lang;
  localStorage.setItem('abrera.lang', lang);
  document.documentElement.setAttribute('lang', lang);
  applyI18n();
  window.I18N_STATE.listeners.forEach((fn) => fn(lang));
};

window.onLangChange = function (fn) {
  window.I18N_STATE.listeners.push(fn);
};

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const val = window.t(key);
    if (/<[a-z]/i.test(val)) el.innerHTML = val;
    else el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.setAttribute('placeholder', window.t(key));
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    el.setAttribute('aria-label', window.t(key));
  });
}
window.applyI18n = applyI18n;
